def voolume_trapesium():
    alas_atas = float(input("masukkan alas atasnya (cm)"))
    alas_bawah = float(input("masukkan alas bawahnya (cm)"))
    tinggi = float(input("masukkan tingginya (cm)"))
    panjang = float(input('masukkan panjangnya (cm)'))
    return 1/2*(alas_atas + alas_bawah)*tinggi*panjang


    